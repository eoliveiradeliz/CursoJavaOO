package modulo01;

//Programa 01.13
//Exemplo de comando if e do comando else
public class ExemploIfElse {

	public static void main(String[] args) {
		int var1 = 10;
		int var2 = 20;
		
		//modo de uso if: (condição )
		
		if (var1 > var2) {
			//bloco de comandos if
			System.out.println("var1 é maior que var2");
		}else  { //bloco de comandos else
			System.out.println("var1 é menor que var2");
		}

	}

}
